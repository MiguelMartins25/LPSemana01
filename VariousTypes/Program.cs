using System;

namespace VariousTypes
{
    public class Program
    {
        private static void Main(string[] args)
        {
            int number1 = 1;
            Console.WriteLine(number1);
            uint number2 = 2U;
            Console.WriteLine(number2);
            char symbol1 = '\uF480';
            Console.WriteLine(symbol1);
            char symbol2 = '\uF940';
            Console.WriteLine(symbol2);
            double real1 = 1.5;
            Console.WriteLine(real1);
            float real2 = 2.25F;
            Console.WriteLine(real2);
            decimal real3 = 3.125M;
            Console.WriteLine(real3);
            bool Verdade = true;
            Console.WriteLine(Verdade);
            bool Falcidade = false;
            Console.WriteLine(Falcidade);
        }
    }
}